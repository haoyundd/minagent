class SearchTool(Tool):
    name = "search"
    description = "搜索互联网信息。输入关键词，返回相关结果。"
    risk_level = "mock"
    stateful = False
    parameters = {
        "type": "object",
        "properties": {
            "query": {
                "type": "string",
                "description": "搜索关键词",
            }
        },
        "required": ["query"],
    }

    def execute(self, query: str):
        """Mock 搜索：多关键词子串匹配。

        遍历所有关键词，query 包含任意一个即视为命中。
        这种方式比精确匹配（key == query）更鲁棒，
        因为 LLM 生成的搜索词通常带有额外修饰词。
        """
        query_lower = query.lower().strip()
        for keywords, content in MOCK_RESULTS.items():
            for kw in keywords:
                if kw in query_lower:
                    return {"query": query, "results": [content]}
        return {"query": query, "results": ["未找到相关结果，请尝试其他关键词。"]}
